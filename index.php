                            <div class="tab-content">
                                <div id="about" class="container tab-pane active">
                                Motivated by the increasing number of miscarriages and the poor health treatment of 
                                pregnant women in sub-Saharan Africa, we intend to develop an online health intervention 
                                system for maternity patients through an online web application. We also analysed the social 
                                and economic value that specific current maternal intervention programs already have in developing 
                                ecosystems such as Ghana, Togo, and Cameroon. We have developed a framework to effectively manage 
                                pregnant women's health based on current models and approaches. We believe that women in need of 
                                maternal health care should be able to self-service instead of waiting or asking for mobility assistance 
                                to get them to the hospital.
                                </div>
                                <div id="mission" class="container tab-pane fade">
                                This project will only cover developing and implementing an online health intervention solution for 
                                maternity patients in Adamawa State. The project is slated to occur during the Spring 2022 semester 
                                and will last within that time frame. This project is geographically limited to the American University 
                                of Nigeria (AUN) campus in Adamawa State. The AUN environment will be used for all phases of the project, 
                                from planning through deployment.
                                </div>
                                <div id="vision" class="container tab-pane fade">
                                    To ensure that every pregnant woman lives to enjoy the life of a mother and also ensure that
                                    every baby don't die during the pregnancy stage.
                                </div>
                                <div id="objectives" class="container tab-pane fade">
                                    To solve the many challenges maternal patients face in Adamawa State. 
                                    The system will also serve as a checkpoint and reference source for scholars interested 
                                    in improving the current healthcare infrastructure in their locality.
                                </div>
                                <div id="values" class="container tab-pane fade">
                                    Love.
                                    <br />
                                    Honesty.
                                    <br />
                                    Integrity.
                                </div>
                                <div id="target" class="container tab-pane fade">
                                    Pregnant and Breastfeeding Mothers in Rural Communities of Adamawa State.
                                    <br />
                                    But for the sake of testing the system, it will be limited to AUN community for now.
                                </div>
